% ========================
%  사실(Facts) 선언
% ========================
% 형식: 술어(인자). ← 반드시 마침표(.)로 끝남

male(허생원).      % 허생원은 남자다
male(동이).        % 동이는 남자다
female(분이).      % 분이는 여자다

% ⚠️ 오타 주의! 쉼표(,)가 아니라 점(.)을 쓰면 안 됨
married(허생원, 분이).   % 허생원과 분이는 결혼했다
parent(분이, 동이).      % 분이는 동이의 부모다


% ========================
%  규칙(Rules) 선언
% ========================
% 형식: 머리 :- 몸.  (머리가 참이려면, 몸이 참이어야 한다)
% :- 는 "if" 로 읽으면 됨

% mother(X, Y) 규칙:
% "X가 Y의 엄마다" 라는 게 언제 참일까?
% → X가 여자이고(female), X가 Y의 부모(parent)일 때!

mother(X, Y) :-
    female(X),     % X가 여자이고
    parent(X, Y).  % X가 Y의 부모이면
                   % → X는 Y의 엄마다

father(X, Y) :-
    male(X),
    married(X, Z),
    parent(Z, Y).


